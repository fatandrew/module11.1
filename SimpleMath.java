public class SimpleMath {
public int add(final int numberA, final int numberB){return numberA + numberB;}
public int adddiv(final int numberA, final int numberB, final int numberC){return numberA + numberB;}
public int multdiv(final int numberA, final int numberB, final int numberC){return numberA * numberB / numberC;}
public int sub(final int numberA, final int numberB){return numberA - numberB;}
public int mult(final int numberA, final int numberB){return numberA * numberB;}
public int div(final int numberA, final int numberB){return numberA / numberB;}

}
